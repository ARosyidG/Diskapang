<u><strong><h3>MAKLUMAT PELAYANAN INFORMASI PUBLIK</h3></strong></u>
<b>Kami berupaya memberikan Pelayanan Informasi Publik dengan sungguh-sungguh untuk dapat:</b>
<ol>
    <li>Memberikan pelayanan informasi yang cepat dan tepat waktu</li>
    <li>Memberikan kemudahan dalam mendapatkan informasi publik yang diperlukan</li>
    <li>Menyediakan dan memberika informasi publik yang akurat, benar dan tidak menyesatkan</li>
    <li>Menyediakan daftar informasi publik untuk informasi yang wajib disediakan dan umum</li>
    <li>Merespon dengan cepat permintaan informasi dan keberatan atas informasi publik yang disampaikan baik secara langsung maupun melalui media</li>
    <li>Menyiapkan petugas informasi yang berdedikasi dan siap melayani</li>
    <li>Melakukan pengawasan internal dan evaluasi kinerja pelaksana</li>
</ol>
