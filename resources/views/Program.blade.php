            <p>Program yang terkait peningkatan ketahanan pangan secara implisit dijabarkan didalam dokumen Rencana Pembangunan Jangka Menengah Daerah (RPJMD) Provinsi NTB Tahun 2013-2018 pada misi ke 5 yaitu meningkatkan kesejahteraan masyarakat, mempercepat penurunan kemiskinan dan mengembangkan keunggulan daerah. Indikator keberhasilan pelaksanaan program dan kebijakan tersebut salah satunya tergantung pada kondisi ketahanan pangan dan gizi wilayah yang tangguh.</p>
            <p>Dalam rangka meningkatkan intervensi sasaran secara geografis dan melakukan pemantuan kondisi yang terkait dengan ketahanan pangan dan gizi wilayah, dibutuhkan satu alat yang berisi data ketahanan pangan dan gizi yang komprehensif dan terupdate, yang dikenal dengan nama Peta Ketahanan dan Kerentanan Pangan Provinsi NTB (Food Security and Vulnerability Atlas/FSVA). Pemerintah  Provinsi NTB melalui Dewan Ketahanan Pangan (DKP) Provinsi NTB  dengan dukungan teknis dan pendanaan dari  United Nations-World Food Programme (WFP) Indonesia, melakukan pemutakhiran Peta Ketahanan dan Kerentanan Pangan Provinsi NTB Tahun 2015.</p>
            <p>Peta tersebut digunakan sebagai alat untuk meningkatkan akurasi penentuan sasaran dan alokasi sumberdaya melalui penyediaan informasi penting bagi para penentu kebijakan didalam proses perencanaan dan penyusunan prioritas program/kebijakan untuk mengurangi kerawanan pangan dan gizi di masa mendatang dari setiap wilayah kabupaten melalui rincian kondisi kecamatan.</p>
            <p>Dalam rangka melakukan analisis yang komprehensif terhadap situasi ketahanan pangan dan gizi yang bersifat multi dimensi, maka ditentukan 9 indikator ketahanan pangan dan gizi.  Indikator-indikator ini dipilih berdasarkan ketersediaan data dan mewakili aspek utama dari 3 pilar ketahanan pangan yaitu:  ketersediaan pangan, akses ke pangan dan pemanfaatan pangan.

            </p>
            <p>Sebagai tambahan analisis setiap indikator individu, analisa komposit juga dilakukan untuk menggambarkan situasi ketahanan pangan dan gizi secara keseluruhan dimana seluruh kecamatan dikelompokkan kedalam enam prioritas. Kecamatan-kecamatan di Prioritas 1-3 digambarkan dalam 3 gradasi warna merah yang menggambarkan kondisi yang cenderung rentan terhadap kerawanan pangan dan gizi sedangkan Prioritas 4-6 digambarkan dalam 3 gradasi warna hijau tergolong kecamatan-kecamatan yang tahan pangan. Penting untuk diingat bahwa tidak semua penduduk di kecamatan-kecamatan prioritas tinggi (Prioritas 1-3) tergolong rawan pangan, demikian juga tidak semua penduduk di kecamatan-kecamatan prioritas rendah (Prioritas 4-6) tergolong tahan pangan.</p>
            <p>Analisis ketahanan dan kerentanan pangan dan gizi ini dilengkapi juga dengan analisis kerentanan terhadap kerawanan pangan yang berkaitan dengan faktor iklim yang meliputi: data kejadian bencana alam yang memiliki dampak terhadap ketahanan pangan, estimasi hilangnya produksi padi yang disebabkan oleh banjir, kekeringan dan OPT, laju deforestasi hutan dan kekuatan pengaruh El NiÃ±o/Southern Oscillation (ENSO) yang berakibat terhadap variabilitas curah hujan.</p>
