                PUPM (Pengembangan Usaha Pangan Masyarakat)/TTI (Toko Tani Indonesia) dibentuk dalam rangka menjaga stabilitas pasokan dan harga pangan pokok strategis, rantai distribusi pemasaran yang terintegrasi agar lebih efisien, harga konsumen dapat ditransmisikan dengan baik kepada harga petani (produsen), informasi pasar antar wilayah berjalan dengan baik, mencegah terjadinya Patron-Client (pemasukan pangan ke pasar suatu wilayah hanya boleh dipasok oleh pelaku usaha tertentu), dan mencegah penyalahgunaan marketpower oleh pelaku usaha tertentu.
